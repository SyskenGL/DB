    private void jRadioButtonEqualStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jRadioButtonEqualStateChanged
     
      Binding legame = bindingGroup.getBinding("bindSchema");
      if (legame.isBound()) {
         legame.unbind();
      } else {
         legame.bind();
      }
     
    }//GEN-LAST:event_jRadioButtonEqualStateChanged

