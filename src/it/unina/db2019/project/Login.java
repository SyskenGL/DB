    private void jButtonEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnterActionPerformed
       DBConnection.host = jTextFieldHost.getText();
       try {
          DBConnection.port = Integer.parseInt(jTextFieldPort.getText());
       } catch (NumberFormatException e) {
          JOptionPane.showMessageDialog(this, "La porta deve essere un numero!",
                                        "Errore", JOptionPane.ERROR_MESSAGE);
       }
       if(DBConnection.port < 0 || DBConnection.port > 65535){
          JOptionPane.showMessageDialog(this, "La porta deve essere compresa tra 0 e 65535!",
                                        "Errore", JOptionPane.ERROR_MESSAGE);
       }
       DBConnection.schema = jTextFieldSchema.getText();
       DBConnection.sid = jTextFieldSID.getText();
       DBConnection.user = jTextFieldUser.getText();
       DBConnection.password = new String(jPasswordField.getPassword());
       try {
          DBConnection.setConnection(DBConnection.newConnection());
          pressed = LOGIN;
          dispose();
       } catch (SQLException e) {
          // Database.mostraErroriSwing(this, e);
          JOptionPane.showMessageDialog(this, "Connessione fallita!",
                                        "Errore", JOptionPane.ERROR_MESSAGE);
          pressed = ERR;
          jPasswordField.setText("");
       }
    }//GEN-LAST:event_jButtonEnterActionPerformed

