    @Override
    protected PreparedStatement opInserisci(Connection c)
           throws SQLException{
                String cmdIns;
        PreparedStatement st;
        cmdIns = "insert into " + DBConnection.schema + ".COMPOSIZIONE (ID_Composizione,FK_Strutturato,"
                + "FK_Tipo)  values(?,?,?)";
        st = c.prepareStatement(cmdIns);
        if(getCodice().getText().equals("")){
            st.setNull(1, Types.INTEGER);
        } else{
            st.setInt(1, Integer.valueOf(getCodice().getText()));
        }
        st.setInt(2, Integer.valueOf(jTextFieldStrutturato.getText()));
        st.setInt(3, Integer.valueOf(jTextFieldTipo.getText()));
        return st;
    }

    @Override
    protected PreparedStatement opModifica(Connection c)
           throws SQLException{
        PreparedStatement st = null;
        if(!getCodice().getText().equals("")){
          String cmdUp;
          cmdUp = "update " + DBConnection.schema + ".COMPOSIZIONE "
                          + " set FK_Strutturato = ?, " 
                          + "     FK_Tipo = ? "
                          + "where     ID_Composizione = ? ";;
          st = c.prepareStatement(cmdUp);
          st.setInt(1, Integer.valueOf(jTextFieldStrutturato.getText()));
          st.setInt(2, Integer.valueOf(jTextFieldTipo.getText()));
          st.setInt(3, Integer.valueOf(getCodice().getText()));
        } else{
              JOptionPane.showMessageDialog(this, "Nessun record selezionato!",
                                            "Errore", JOptionPane.ERROR_MESSAGE);
        }
        return st;
    }

    @Override
    protected PreparedStatement opRicerca(Connection c)
           throws SQLException{
        PreparedStatement st = null;
        String cmdSrc;
        int index = 1;
        Pattern pat;
        Matcher match;
        cmdSrc = "select * from " + DBConnection.schema + ".COMPOSIZIONE where";
        if (getCodice().getText().length() > 0) {
           cmdSrc += " ID_Composizione = ? and";
        }
        if (jTextFieldStrutturato.getText().length() > 0) {
           cmdSrc += " FK_Strutturato = ? and";
        }
        if (jTextFieldTipo.getText().length() > 0) {
           cmdSrc += " FK_Tipo = ? and";
        }
        pat = Pattern.compile("(where|and)$"); 
        match = pat.matcher(cmdSrc);
        cmdSrc = match.replaceAll("");
        cmdSrc += " ORDER BY ID_COMPOSIZIONE";
        try {
           st = c.prepareStatement(cmdSrc, ResultSet.TYPE_SCROLL_INSENSITIVE,
                   ResultSet.CONCUR_READ_ONLY); 
        } catch (SQLException e) {
        }
        if(st != null){
          if (getCodice().getText().length() > 0) {
              st.setInt(index++, Integer.valueOf(getCodice().getText()));
          }
          if (jTextFieldStrutturato.getText().length() > 0) {
              st.setInt(index++, Integer.valueOf(jTextFieldStrutturato.getText()));
          }
          if (jTextFieldTipo.getText().length() > 0) {
              st.setInt(index++, Integer.valueOf(jTextFieldTipo.getText()));
          }
        }
        return st; 
    }

    @Override
    protected PreparedStatement opElimina(Connection c)
           throws SQLException{
        PreparedStatement st = null;
        if(!getCodice().getText().equals("")){
            String cmdDel;
            cmdDel = "delete from " + DBConnection.schema + ".COMPOSIZIONE where "
                      + "ID_Composizione = ? ";
            st = c.prepareStatement(cmdDel);
            st.setInt(1, Integer.valueOf(getCodice().getText()));
        } else{
            JOptionPane.showMessageDialog(this, "Nessun record selezionato!",
                                          "Errore", JOptionPane.ERROR_MESSAGE);
        }
        return st;
    }
    
