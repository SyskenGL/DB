   @Override
    protected void reselectPrevRow(){
        if(jTableComposizione.getRowCount() > 0){
            switch(getModalita()){
                case MODIFICA:
                    jTableComposizione.setRowSelectionInterval(selectedRow, selectedRow);
                    break;
                case INSERISCI:
                    jTableComposizione.setRowSelectionInterval(jTableComposizione.getRowCount()-1, jTableComposizione.getRowCount()-1);
                    break;
                case ELIMINA:
                    if(selectedRow < jTableComposizione.getRowCount()){
                            jTableComposizione.setRowSelectionInterval(selectedRow, selectedRow);
                    } else{
                        jTableComposizione.setRowSelectionInterval(selectedRow-1, selectedRow-1);
                    }     
            }
        }    
    }
    

